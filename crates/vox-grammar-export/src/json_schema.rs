//! JSON Schema emitter for Vox AST nodes (Wave 1 Tasks 36-37).
//!
//! Recursively defines all core Vox AST node types as a JSON Schema draft-2020-12 document.
//! Used by constrained inference tools and MCP clients for structured output generation.

use serde_json::{Value, json};

/// Emit the Vox AST as a JSON Schema (draft-2020-12) document.
///
/// Returns a fully self-contained schema string. All `$defs` are in-lined under
/// `"$defs"` so callers can reference them via `"$ref": "#/$defs/<NodeName>"`.
pub fn emit_json_schema() -> String {
    let schema = build_schema();
    serde_json::to_string_pretty(&schema).unwrap_or_else(|_| "{}".to_string())
}

fn build_schema() -> Value {
    json!({
        "$schema": "https://json-schema.org/draft/2020-12/schema",
        "$id": "https://vox-lang.org/schemas/ast.v1.json",
        "title": "VoxAstNode",
        "description": "Root union of all Vox AST nodes (auto-generated by vox-grammar-export).",
        "$ref": "#/$defs/Module",
        "$defs": {
            "Module": {
                "type": "object",
                "description": "Top-level Vox module (file).",
                "required": ["kind", "decls"],
                "properties": {
                    "kind": { "const": "Module" },
                    "decls": {
                        "type": "array",
                        "items": { "$ref": "#/$defs/Decl" }
                    }
                }
            },
            "Decl": {
                "description": "A top-level declaration.",
                "oneOf": [
                    { "$ref": "#/$defs/FnDecl" },
                    { "$ref": "#/$defs/LetDecl" },
                    { "$ref": "#/$defs/TypeDecl" }
                ]
            },
            "FnDecl": {
                "type": "object",
                "required": ["kind", "name", "params", "body"],
                "properties": {
                    "kind": { "const": "FnDecl" },
                    "name": { "type": "string" },
                    "params": {
                        "type": "array",
                        "items": { "$ref": "#/$defs/Param" }
                    },
                    "return_type": { "$ref": "#/$defs/TypeExpr" },
                    "body": { "$ref": "#/$defs/Block" }
                }
            },
            "Param": {
                "type": "object",
                "required": ["kind", "name", "ty"],
                "properties": {
                    "kind": { "const": "Param" },
                    "name": { "type": "string" },
                    "ty": { "$ref": "#/$defs/TypeExpr" }
                }
            },
            "LetDecl": {
                "type": "object",
                "required": ["kind", "name", "value"],
                "properties": {
                    "kind": { "const": "LetDecl" },
                    "name": { "type": "string" },
                    "ty": { "$ref": "#/$defs/TypeExpr" },
                    "value": { "$ref": "#/$defs/Expr" }
                }
            },
            "TypeDecl": {
                "type": "object",
                "required": ["kind", "name", "ty"],
                "properties": {
                    "kind": { "const": "TypeDecl" },
                    "name": { "type": "string" },
                    "ty": { "$ref": "#/$defs/TypeExpr" }
                }
            },
            "TypeExpr": {
                "description": "A type expression.",
                "oneOf": [
                    {
                        "type": "object",
                        "required": ["kind", "name"],
                        "properties": {
                            "kind": { "const": "NamedType" },
                            "name": { "type": "string" }
                        }
                    },
                    {
                        "type": "object",
                        "required": ["kind", "base", "args"],
                        "properties": {
                            "kind": { "const": "GenericType" },
                            "base": { "type": "string" },
                            "args": {
                                "type": "array",
                                "items": { "$ref": "#/$defs/TypeExpr" }
                            }
                        }
                    },
                    {
                        "type": "object",
                        "required": ["kind", "fields"],
                        "properties": {
                            "kind": { "const": "TupleType" },
                            "fields": {
                                "type": "array",
                                "items": { "$ref": "#/$defs/TypeExpr" }
                            }
                        }
                    },
                    {
                        "type": "object",
                        "required": ["kind", "fields"],
                        "properties": {
                            "kind": { "const": "RecordType" },
                            "fields": {
                                "type": "array",
                                "items": { "$ref": "#/$defs/TypeField" }
                            }
                        }
                    }
                ]
            },
            "TypeField": {
                "type": "object",
                "required": ["name", "ty"],
                "properties": {
                    "name": { "type": "string" },
                    "ty": { "$ref": "#/$defs/TypeExpr" }
                }
            },
            "Block": {
                "type": "object",
                "required": ["kind", "stmts"],
                "properties": {
                    "kind": { "const": "Block" },
                    "stmts": {
                        "type": "array",
                        "items": { "$ref": "#/$defs/Stmt" }
                    },
                    "tail_expr": { "$ref": "#/$defs/Expr" }
                }
            },
            "Stmt": {
                "description": "A statement inside a block.",
                "oneOf": [
                    { "$ref": "#/$defs/LetDecl" },
                    { "$ref": "#/$defs/Expr" }
                ]
            },
            "Expr": {
                "description": "An expression.",
                "oneOf": [
                    { "$ref": "#/$defs/Literal" },
                    { "$ref": "#/$defs/RecordLit" },
                    { "$ref": "#/$defs/TupleLit" },
                    { "$ref": "#/$defs/ArrayLit" },
                    {
                        "type": "object",
                        "required": ["kind", "name"],
                        "properties": {
                            "kind": { "const": "Ident" },
                            "name": { "type": "string" }
                        }
                    },
                    { "$ref": "#/$defs/CallExpr" },
                    { "$ref": "#/$defs/FieldAccess" },
                    { "$ref": "#/$defs/BinaryExpr" },
                    { "$ref": "#/$defs/IfExpr" },
                    { "$ref": "#/$defs/Block" }
                ]
            },
            "Literal": {
                "oneOf": [
                    {
                        "type": "object",
                        "required": ["kind", "value"],
                        "properties": {
                            "kind": { "const": "IntLit" },
                            "value": { "type": "integer" }
                        }
                    },
                    {
                        "type": "object",
                        "required": ["kind", "value"],
                        "properties": {
                            "kind": { "const": "FloatLit" },
                            "value": { "type": "number" }
                        }
                    },
                    {
                        "type": "object",
                        "required": ["kind", "value"],
                        "properties": {
                            "kind": { "const": "StringLit" },
                            "value": { "type": "string" }
                        }
                    },
                    {
                        "type": "object",
                        "required": ["kind", "value"],
                        "properties": {
                            "kind": { "const": "BoolLit" },
                            "value": { "type": "boolean" }
                        }
                    }
                ]
            },
            "RecordLit": {
                "type": "object",
                "required": ["kind", "fields"],
                "properties": {
                    "kind": { "const": "RecordLit" },
                    "fields": {
                        "type": "array",
                        "items": { "$ref": "#/$defs/FieldInit" }
                    }
                }
            },
            "FieldInit": {
                "type": "object",
                "required": ["name", "value"],
                "properties": {
                    "name": { "type": "string" },
                    "value": { "$ref": "#/$defs/Expr" }
                }
            },
            "TupleLit": {
                "type": "object",
                "required": ["kind", "elements"],
                "properties": {
                    "kind": { "const": "TupleLit" },
                    "elements": {
                        "type": "array",
                        "items": { "$ref": "#/$defs/Expr" }
                    }
                }
            },
            "ArrayLit": {
                "type": "object",
                "required": ["kind", "elements"],
                "properties": {
                    "kind": { "const": "ArrayLit" },
                    "elements": {
                        "type": "array",
                        "items": { "$ref": "#/$defs/Expr" }
                    }
                }
            },
            "CallExpr": {
                "type": "object",
                "required": ["kind", "callee", "args"],
                "properties": {
                    "kind": { "const": "CallExpr" },
                    "callee": { "$ref": "#/$defs/Expr" },
                    "args": {
                        "type": "array",
                        "items": { "$ref": "#/$defs/Expr" }
                    }
                }
            },
            "FieldAccess": {
                "type": "object",
                "required": ["kind", "base", "field"],
                "properties": {
                    "kind": { "const": "FieldAccess" },
                    "base": { "$ref": "#/$defs/Expr" },
                    "field": { "type": "string" }
                }
            },
            "BinaryExpr": {
                "type": "object",
                "required": ["kind", "left", "op", "right"],
                "properties": {
                    "kind": { "const": "BinaryExpr" },
                    "left": { "$ref": "#/$defs/Expr" },
                    "op": {
                        "type": "string",
                        "enum": ["+", "-", "*", "/", "==", "!=", "<", ">", "<=", ">="]
                    },
                    "right": { "$ref": "#/$defs/Expr" }
                }
            },
            "IfExpr": {
                "type": "object",
                "required": ["kind", "condition", "then_block"],
                "properties": {
                    "kind": { "const": "IfExpr" },
                    "condition": { "$ref": "#/$defs/Expr" },
                    "then_block": { "$ref": "#/$defs/Block" },
                    "else_block": { "$ref": "#/$defs/Block" }
                }
            }
        }
    })
}
