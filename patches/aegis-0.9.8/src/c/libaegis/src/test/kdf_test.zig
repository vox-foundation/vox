const kdf = @cImport(@cInclude("keccak.h"));
const std = @import("std");
const testing = std.testing;

const context = "aegis-raf-kdf-v1";

test "aegis_kdf_128 KAT" {
    const master_key = [16]u8{ 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f };
    const file_id = [24]u8{ 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17 };
    const expected = [32]u8{
        0x4c, 0xc4, 0x75, 0x9d, 0x9f, 0x10, 0xcf, 0x1b,
        0x39, 0x1c, 0xfc, 0x22, 0x0d, 0x9b, 0x32, 0x9d,
        0x07, 0x65, 0x65, 0x37, 0x47, 0x7b, 0x69, 0x65,
        0x70, 0x8f, 0x98, 0xf3, 0x5e, 0x78, 0x07, 0xaf,
    };

    var out: [32]u8 = undefined;
    kdf.aegis_kdf_128(&out, out.len, context, context.len, &master_key, master_key.len, &file_id, file_id.len);

    try testing.expectEqualSlices(u8, &expected, &out);
}

