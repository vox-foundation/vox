/**
 * Reads contracts/mcp/tool-registry.canonical.yaml and emits a TS constant list.
 * Run from repo: node apps/editor/vox-vscode/scripts/generate-mcp-tool-registry.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
// scripts/ -> vox-vscode/ -> editor/ -> apps/ -> repository root
const repoRoot = path.resolve(__dirname, '..', '..', '..', '..');
const yamlPath = path.join(repoRoot, 'contracts', 'mcp', 'tool-registry.canonical.yaml');
const outPath = path.join(__dirname, '..', 'src', 'core', 'mcpToolRegistry.generated.ts');

const raw = fs.readFileSync(yamlPath, 'utf8');
const names = [];
// Quoted or bare tool id after `tools:` list items (see contracts/mcp/tool-registry.canonical.yaml).
const re = /^\s*-\s*name:\s*(?:"([^"]+)"|([a-zA-Z0-9_:]+))/gm;
let m;
while ((m = re.exec(raw)) !== null) {
    names.push(m[1] ?? m[2]);
}
if (names.length === 0) {
    throw new Error(`No tool names parsed from ${yamlPath}`);
}

/** Tools the VS Code workspace UI expects at runtime for full functionality (warn if absent from list_tools). */
const extensionExpected = [
    'vox_orchestrator_status',
    'vox_poll_events',
    'vox_budget_status',
    'vox_pipeline_status',
    'vox_a2a_tasks',
    'vox_oplog',
    'vox_cost_history',
    'vox_list_models',
    'vox_compiler::ast_inspect',
    // Oratio speech commands (palette + webview capture) degrade without these tools.
    'vox_oratio_transcribe',
    'vox_speech_to_code',
];

for (const n of extensionExpected) {
    if (!names.includes(n)) {
        throw new Error(`MCP_EXTENSION_EXPECTED_TOOLS entry "${n}" not in canonical YAML`);
    }
}

const body = `// @generated by scripts/generate-mcp-tool-registry.mjs — do not edit.

/** Canonical MCP tool names from contracts/mcp/tool-registry.canonical.yaml */
export const MCP_CANONICAL_TOOL_NAMES = ${JSON.stringify(names, null, 4)} as const;

export type McpCanonicalToolName = (typeof MCP_CANONICAL_TOOL_NAMES)[number];

export const MCP_CANONICAL_TOOL_SET: ReadonlySet<string> = new Set<string>(MCP_CANONICAL_TOOL_NAMES);

/** Subset checked on connect against runtime list_tools (optional warning only). */
export const MCP_EXTENSION_EXPECTED_TOOLS = ${JSON.stringify(extensionExpected, null, 4)} as const;
`;

fs.writeFileSync(outPath, body, 'utf8');
console.log(`Wrote ${names.length} tools -> ${outPath}`);
